ls - List directory contents
cp - Copy files and directories
mv - Move or rename files and directories
rm - Remove files and directories
mkdir - Create directories
rmdir - Remove empty directories
cat - Concatenate and display file content
grep - Search for patterns in files
find - Search for files and directories
ps - Display running processes
top - Monitor system processes
chmod - Change file permissions
chown - Change file ownership
sudo - Execute a command with superuser privileges
apt-get (or apt) - Package management tool (Ubuntu/Debian)
yum - Package management tool (RHEL/CentOS)
systemctl - Control system services (systemd-based systems)
ifconfig (or ip) - Network interface configuration
ping - Send ICMP echo requests to a network host
ssh - Secure shell remote login